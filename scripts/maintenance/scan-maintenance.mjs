function loadBaseline() {
  if (!baselineArg) return undefined;
  const baselinePath = path.resolve(root, baselineArg.split('=')[1]);
  const baseline = JSON.parse(fs.readFileSync(baselinePath, 'utf8'));
  return {
    path: rel(baselinePath),
    criticalHigh: baseline.criticalHigh ?? 0,
    summary: baseline.summary ?? {},
  };
}

function rel(file) {
  return path.relative(root, file).replaceAll(path.sep, '/');
}

function walk(dir, out = []) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    if (entry.isDirectory()) {
      if (!skipDirs.has(entry.name)) walk(path.join(dir, entry.name), out);
      continue;
    }
    const file = path.join(dir, entry.name);
    if (codeExts.has(path.extname(file))) out.push(file);
  }
  return out;
}

function walkScope(scopeRoot) {
  if (!fs.existsSync(scopeRoot)) return [];
  const stats = fs.statSync(scopeRoot);
  if (stats.isDirectory()) return walk(scopeRoot);
  if (stats.isFile() && codeExts.has(path.extname(scopeRoot))) {
    return [scopeRoot];
  }
  return [];
}

function read(file) {
  return fs.readFileSync(file, 'utf8').replace(/\r\n/g, '\n');
}

function isTestFile(file) {
  const r = rel(file);
  return /(^|\/)__tests__\//.test(r) || /\.(spec|test)\.[cm]?[tj]sx?$/.test(r);
}

function hasGeneratedHeader(text, file) {
  if (/\.(generated|gen)\.ts$|\.d\.ts$/.test(file)) return true;
  return text
    .split('\n')
    .slice(0, 5)
    .some((line) =>
      /do not edit|auto-generated|generated by|this file is generated/i.test(
        line,
      ),
    );
}

function isExcludedBloatFile(file, text) {
  const r = rel(file);
  return (
    isTestFile(file) ||
    hasGeneratedHeader(text, file) ||
    /\.fragment\.[cm]?[tj]sx?$/.test(file) ||
    /\.(seed|seeds|fixture|fixtures)\.ts$/.test(file) ||
    /(^|\/)(seeds|fixtures|migrations|mocks)\//.test(r) ||
    /\.mock\.ts$|\.spec-helpers\.tsx?$|\.spec\.helpers\.tsx?$|\.test-helpers\.tsx?$/.test(
      file,
    ) ||
    /package-lock\.json$|yarn\.lock$/.test(file)
  );
}

function isStoryFile(file) {
  return /\.(stories|story)\.[cm]?[tj]sx?$/.test(file);
}

function isDuplicateScanExcludedFile(file, text) {
  const r = rel(file);
  return (
    isTestFile(file) ||
    isStoryFile(file) ||
    hasGeneratedHeader(text, file) ||
    /\.(seed|seeds|fixture|fixtures)\.ts$/.test(file) ||
    /(^|\/)(__fixtures__|fixtures|mocks|testing)\//.test(r) ||
    /(^|\/)types\/contracts\/generated\//.test(r)
  );
}

function isImportClauseLine(line) {
  return (
    /^(import|export)\b/.test(line) ||
    /^} from ['"`]/.test(line) ||
    /^[A-Za-z_$][\w$]*(?:\s+as\s+[A-Za-z_$][\w$]*)?,?$/.test(line)
  );
}

function isDataOrDeclarationLine(line) {
  const dataLine = line.replace(/^\{\s*/, '');
  return (
    /^(readonly\s+)?[A-Za-z_$][\w$?]*\??:\s*.+[,;]?$/.test(dataLine) ||
    /^\[[^\]]+\]:\s*[^=]+[,;]?$/.test(dataLine) ||
    /^[A-Za-z_$][\w$.]*(?:\.[A-Z0-9_]+)?,?$/.test(line) ||
    /^['"`][^'"`]*['"`],?$/.test(line) ||
    /^[}\])][,;]?$/.test(line)
  );
}

function isJsxPropOrTagLine(line) {
  return (
    /^[A-Za-z_$][\w$.-]*=/.test(line) ||
    /^(aria-[\w-]+|data-[\w-]+|role)=/.test(line) ||
    /^<\/?[A-Za-z]/.test(line) ||
    /^\/?>$/.test(line) ||
    /^\{[A-Za-z_$][\w$.]*(?:\([^)]*\))?\}$/.test(line)
  );
}

function isLiteralConsoleLine(line) {
  return /^console\.(log|warn|error)\(\s*(?:'[^']*'|"[^"]*"|`[^`]*`)\s*\);?$/.test(
    line,
  );
}

function isLowSignalDuplicateBlock(lines) {
  return (
    lines.every(isImportClauseLine) ||
    lines.every(isDataOrDeclarationLine) ||
    lines.every(isJsxPropOrTagLine) ||
    lines.every(isLiteralConsoleLine)
  );
}

function countLoc(text, ext) {
  let inBlock = false;
  let count = 0;
  for (const raw of text.split('\n')) {
    let line = raw.trim();
    if (!line) continue;
    if (ext === '.py' && /^(#|"""|''')/.test(line)) continue;
    if (inBlock) {
      if (line.includes('*/')) inBlock = false;
      continue;
    }
    if (line.startsWith('/*')) {
      if (!line.includes('*/')) inBlock = true;
      continue;
    }
    if (/^(\/\/|\*)/.test(line)) continue;
    count++;
  }
  return count;
}

function add(category, severity, file, line, message, meta = {}) {
  if (categoryFilter && category !== categoryFilter) return;
  findings.push({
    category,
    severity,
    file: rel(file),
    line,
    message,
    ...meta,
  });
}

function bloatProfile(file, text, ext) {
  const name = path.basename(file).toLowerCase();
  if (ext === '.py') {
    return { category: 'python', thresholds: [200, 300, 500, 800] };
  }
  if (
    /d3|@d3/.test(text) ||
    text.includes('<canvas') ||
    /chart|plot|graph|heatmap|scatter|coordinates/.test(name)
  ) {
    return { category: 'rendering', thresholds: [500, 650, 900, 1200] };
  }
  if (/executor|orchestrator|runner/.test(name)) {
    return { category: 'orchestrator', thresholds: [450, 600, 800, 1100] };
  }
  if (/controller/.test(name) || rel(file).startsWith('src/pages/api/')) {
    return { category: 'route-handler', thresholds: [400, 650, 900, 1200] };
  }
  if (/conformance|validation|validator/.test(name)) {
    return { category: 'validation', thresholds: [400, 700, 1000, 1300] };
  }
  return { category: 'standard', thresholds: [300, 600, 900, 1200] };
}

function classifyBloat(file, text) {
  const ext = path.extname(file);
  if (
    !['.ts', '.tsx', '.py', '.html'].includes(ext) ||
    isExcludedBloatFile(file, text)
  )
    return;
  const { category, thresholds } = bloatProfile(file, text, ext);
  const loc = countLoc(text, ext);
  const severity =
    loc >= thresholds[3]
      ? 'critical'
      : loc >= thresholds[2]
        ? 'high'
        : loc >= thresholds[1]
          ? 'warn'
          : loc >= thresholds[0]
            ? 'info'
            : undefined;
  if (severity)
    add(
      'file-bloat',
      severity,
      file,
      1,
      `${loc} LOC exceeds ${category} ${severity === 'info' ? 'advisory' : 'threshold'}`,
      { loc, fileType: category },
    );
}

function acceptedTypeSuppression(lines, index) {
  const context = lines.slice(Math.max(0, index - 2), index + 3).join(' ');
  return /(framework|rete|next|react|zustand|optional dependency|typing limitation|incompatible|mismatch|stub|placeholder)/i.test(
    context,
  );
}

function scanTypeSafety(file, text) {
  if (
    !typeScriptExts.has(path.extname(file)) ||
    isTestFile(file) ||
    hasGeneratedHeader(text, file)
  )
    return;
  const lines = text.split('\n');
  let inBlockComment = false;
  lines.forEach((line, index) => {
    const trimmed = line.trim();
    if (inBlockComment) {
      if (trimmed.includes('*/')) inBlockComment = false;
      return;
    }
    if (trimmed.startsWith('/*')) {
      if (!trimmed.includes('*/')) inBlockComment = true;
      return;
    }
    if (/^\s*\/\//.test(line)) return;
    const lineNumber = index + 1;
    if (
      /\bas\s+unknown\s+as\b/.test(line) &&
      !acceptedTypeSuppression(lines, index)
    ) {
      add('type-safety', 'high', file, lineNumber, '`as unknown as` cast');
    }
    if (/\bas\s+any\b/.test(line) && !acceptedTypeSuppression(lines, index)) {
      add('type-safety', 'high', file, lineNumber, '`as any` cast');
    }
    if (
      /[:<,]\s*any\b/.test(line) &&
      !/Record<string,\s*any>|eslint-disable/.test(line) &&
      !acceptedTypeSuppression(lines, index)
    ) {
      add('type-safety', 'high', file, lineNumber, '`any` annotation');
    }
  });
}

function scanDeadCode(file, text) {
  const lines = text.split('\n');
  lines.forEach((line, index) => {
    if (/catch\s*(\([^)]*\))?\s*{\s*}/.test(line))
      add('dead-code', 'high', file, index + 1, 'empty catch block');
  });
  for (let i = 0; i < lines.length - 2; i++) {
    const block = lines.slice(i, i + 3);
    if (
      block.every((line) =>
        /^\s*\/\/\s*(if|for|while|const|let|var|return|await|try|switch)\b/.test(
          line,
        ),
      )
    ) {
      add('dead-code', 'high', file, i + 1, 'commented-out code block');
      i += 2;
    }
  }
}

function scanImportHealth(file, text) {
  if (path.basename(file) !== 'index.ts') return;
  const exports = text
    .split('\n')
    .filter((line) => /^\s*export\s+/.test(line)).length;
  if (exports > 20)
    add(
      'import-health',
      'warn',
      file,
      1,
      `barrel file re-exports ${exports} symbols`,
      { exports },
    );
}

function todoAge(file, lineNumber) {
  try {
    const out = execFileSync(
      'git',
      [
        'blame',
        '--line-porcelain',
        `-L${lineNumber},${lineNumber}`,
        '--',
        rel(file),
      ],
      {
        cwd: root,
        encoding: 'utf8',
        stdio: ['ignore', 'pipe', 'ignore'],
      },
    );
    const match = out.match(/^author-time\s+(\d+)/m);
    if (!match) return undefined;
    return Math.floor((Date.now() / 1000 - Number(match[1])) / 86400);
  } catch {
    return undefined;
  }
}

function pythonCommentFragments(text) {
  const fragments = [];
  text.split('\n').forEach((line, index) => {
    const lineNumber = index + 1;
    const hashIndex = line.indexOf('#');
    if (hashIndex >= 0) {
      fragments.push({ lineNumber, text: line.slice(hashIndex) });
    }
  });
  return fragments;
}

function collectHtmlCommentFragments(line, lineNumber, state, fragments) {
  let cursor = 0;
  while (cursor < line.length) {
    if (state.inComment) {
      const end = line.indexOf('-->', cursor);
      fragments.push({
        lineNumber,
        text: end >= 0 ? line.slice(cursor, end) : line.slice(cursor),
      });
      if (end < 0) return;
      state.inComment = false;
      cursor = end + 3;
      continue;
    }

    const start = line.indexOf('<!--', cursor);
    if (start < 0) return;
    const end = line.indexOf('-->', start + 4);
    fragments.push({
      lineNumber,
      text: end >= 0 ? line.slice(start + 4, end) : line.slice(start + 4),
    });
    if (end < 0) {
      state.inComment = true;
      return;
    }
    cursor = end + 3;
  }
}

function htmlCommentFragments(text) {
  const fragments = [];
  const state = { inComment: false };
  const lines = text.split('\n');
  for (let index = 0; index < lines.length; index++) {
    collectHtmlCommentFragments(lines[index], index + 1, state, fragments);
  }
  return fragments;
}

function collectScriptCommentFragments(line, lineNumber, state, fragments) {
  let cursor = 0;
  while (cursor < line.length) {
    if (state.inBlockComment) {
      const end = line.indexOf('*/', cursor);
      fragments.push({
        lineNumber,
        text: end >= 0 ? line.slice(cursor, end) : line.slice(cursor),
      });
      if (end < 0) return;
      state.inBlockComment = false;
      cursor = end + 2;
      continue;
    }

    const lineComment = line.indexOf('//', cursor);
    const blockComment = line.indexOf('/*', cursor);
    if (lineComment < 0 && blockComment < 0) return;
    if (lineComment >= 0 && (blockComment < 0 || lineComment < blockComment)) {
      fragments.push({ lineNumber, text: line.slice(lineComment + 2) });
      return;
    }

    const end = line.indexOf('*/', blockComment + 2);
    fragments.push({
      lineNumber,
      text:
        end >= 0
          ? line.slice(blockComment + 2, end)
          : line.slice(blockComment + 2),
    });
    if (end < 0) {
      state.inBlockComment = true;
      return;
    }
    cursor = end + 2;
  }
}

function scriptCommentFragments(text) {
  const fragments = [];
  const state = { inBlockComment: false };
  const lines = text.split('\n');
  for (let index = 0; index < lines.length; index++) {
    collectScriptCommentFragments(lines[index], index + 1, state, fragments);
  }
  return fragments;
}

function commentFragments(file, text) {
  const ext = path.extname(file);
  if (ext === '.py') return pythonCommentFragments(text);
  if (ext === '.html') return htmlCommentFragments(text);
  return scriptCommentFragments(text);
}

function scanTodos(file, text) {
  commentFragments(file, text).forEach((comment) => {
    if (!/\b(TODO|FIXME|HACK|WORKAROUND)\b/i.test(comment.text)) return;
    const days = todoAge(file, comment.lineNumber);
    const severity =
      days === undefined
        ? 'info'
        : days >= 90
          ? 'high'
          : days >= 30
            ? 'medium'
            : 'info';
    add(
      'stale-todo',
      severity,
      file,
      comment.lineNumber,
      days === undefined
        ? 'unblamed TODO-like comment'
        : `${days} day old TODO-like comment`,
      { days },
    );
  });
}

function expectedTestNames(file) {
  const parsed = path.parse(file);
  const base = path.join(parsed.dir, parsed.name);
  return [
    `${base}.spec${parsed.ext}`,
    `${base}.test${parsed.ext}`,
    path.join(parsed.dir, '__tests__', `${parsed.name}.test${parsed.ext}`),
    path.join(parsed.dir, '__tests__', `${parsed.name}.spec${parsed.ext}`),
  ];
}

function scanTestGap(file) {
  const r = rel(file);
  if (
    !/src\//.test(r) ||
    isTestFile(file) ||
    /\.(types|schema|dto|entity|module)\.ts$/.test(file) ||
    path.basename(file) === 'index.ts'
  )
    return;
  if (
    !/\.(service|controller|component|guard|pipe|interceptor|gateway)\.tsx?$/.test(
      file,
    )
  )
    return;
  if (!expectedTestNames(file).some((candidate) => fs.existsSync(candidate))) {
    add('test-gap', 'high', file, 1, 'no corresponding spec/test file');
  }
}

function functionName(node, source) {
  if (node.name?.getText) return node.name.getText(source);
  if (ts.isVariableDeclaration(node.parent) && node.parent.name)
    return node.parent.name.getText(source);
  return '<anonymous>';
}

function isFunctionLikeNode(node) {
  return (
    ts.isFunctionDeclaration(node) ||
    ts.isMethodDeclaration(node) ||
    ts.isConstructorDeclaration(node) ||
    ts.isFunctionExpression(node) ||
    ts.isArrowFunction(node)
  );
}

function hasModifier(node, modifierText, source) {
  return node.modifiers?.some(
    (modifier) => modifier.getText(source) === modifierText,
  );
}

function isPrivateReadonlyProperty(member, source) {
  return (
    ts.isPropertyDeclaration(member) &&
    hasModifier(member, 'private', source) &&
    hasModifier(member, 'readonly', source)
  );
}

function isPublicMethod(member, source) {
  return (
    ts.isMethodDeclaration(member) &&
    !hasModifier(member, 'private', source) &&
    !hasModifier(member, 'protected', source)
  );
}

function methodName(method, source) {
  return method.name?.getText(source).replace(/^['"]|['"]$/g, '') ?? '';
}

const cohesiveValueObjectMethodNames = new Set([
  'absolute',
  'add',
  'clone',
  'compare',
  'compareTo',
  'divide',
  'equals',
  'format',
  'isNegative',
  'isPositive',
  'isPositiveOrZero',
  'isZero',
  'max',
  'min',
  'multiply',
  'negate',
  'parse',
  'subtract',
  'toJSON',
  'toString',
  'valueOf',
]);

function isAllowedValueObjectMethod(method, source) {
  const name = methodName(method, source);
  if (cohesiveValueObjectMethodNames.has(name)) return true;
  return (
    hasModifier(method, 'static', source) &&
    (/^from[A-Z_]/.test(name) || name === 'of')
  );
}

function isCohesiveValueObjectClass(node, source, publicMethods) {
  const privateReadonlyProperties = node.members.filter((member) =>
    isPrivateReadonlyProperty(member, source),
  );
  if (
    privateReadonlyProperties.length === 0 ||
    privateReadonlyProperties.length > 2
  ) {
    return false;
  }

  const mutablePublicProperties = node.members.filter(
    (member) =>
      ts.isPropertyDeclaration(member) &&
      !hasModifier(member, 'private', source) &&
      !hasModifier(member, 'protected', source) &&
      !(
        hasModifier(member, 'static', source) &&
        hasModifier(member, 'readonly', source)
      ),
  );
  if (mutablePublicProperties.length > 0) return false;

  return publicMethods.every((method) =>
    isAllowedValueObjectMethod(method, source),
  );
}

function scanTsAst(file, text) {
  if (!scriptExts.has(path.extname(file)) || hasGeneratedHeader(text, file))
    return;
  const source = ts.createSourceFile(file, text, ts.ScriptTarget.Latest, true);
  function lineOf(node) {
    return source.getLineAndCharacterOfPosition(node.getStart(source)).line + 1;
  }
  function visitFunction(node) {
    let decisions = 0;
    let maxDepth = 0;
    function walkNode(child, depth = 0) {
      if (isFunctionLikeNode(child)) return;
      const isDecision =
        ts.isIfStatement(child) ||
        ts.isCaseClause(child) ||
        ts.isDefaultClause(child) ||
        ts.isCatchClause(child) ||
        ts.isConditionalExpression(child) ||
        ts.isForStatement(child) ||
        ts.isForInStatement(child) ||
        ts.isForOfStatement(child) ||
        ts.isWhileStatement(child) ||
        ts.isDoStatement(child);
      if (isDecision) {
        decisions++;
        maxDepth = Math.max(maxDepth, depth + 1);
      }
      if (
        ts.isBinaryExpression(child) &&
        ['&&', '||', '??'].includes(child.operatorToken.getText(source))
      )
        decisions++;
      ts.forEachChild(child, (grandchild) =>
        walkNode(grandchild, isDecision ? depth + 1 : depth),
      );
    }
    if (node.body) walkNode(node.body);
    const complexity = 1 + decisions;
    const name = functionName(node, source);
    if (complexity >= 31)
      add(
        'complexity',
        'critical',
        file,
        lineOf(node),
        `${name} complexity ${complexity}`,
        { complexity, symbol: name },
      );
    else if (complexity >= 21)
      add(
        'complexity',
        'high',
        file,
        lineOf(node),
        `${name} complexity ${complexity}`,
        { complexity, symbol: name },
      );
    if (maxDepth >= 6)
      add(
        'code-smell',
        'critical',
        file,
        lineOf(node),
        `${name} nesting depth ${maxDepth}`,
        { depth: maxDepth, symbol: name },
      );
    else if (maxDepth >= 5)
      add(
        'code-smell',
        'high',
        file,
        lineOf(node),
        `${name} nesting depth ${maxDepth}`,
        { depth: maxDepth, symbol: name },
      );
    const params = node.parameters?.length ?? 0;
    if (params >= 7)
      add(
        'code-smell',
        'high',
        file,
        lineOf(node),
        `${name} has ${params} parameters`,
        { params, symbol: name },
      );
    const boolParams = (node.parameters ?? []).filter((param) =>
      /\bboolean\b/.test(param.type?.getText(source) ?? ''),
    ).length;
    if (boolParams >= 3)
      add(
        'code-smell',
        'high',
        file,
        lineOf(node),
        `${name} has ${boolParams} boolean parameters`,
        { boolParams, symbol: name },
      );
  }
  function visit(node) {
    if (isFunctionLikeNode(node)) {
      visitFunction(node);
    }
    if (ts.isSwitchStatement(node)) {
      const cases = node.caseBlock.clauses.length;
      if (cases >= 8)
        add(
          'design-violation',
          'high',
          file,
          lineOf(node),
          `switch statement has ${cases} cases`,
          { cases },
        );
    }
    if (ts.isClassDeclaration(node)) {
      const publicMethods = node.members.filter((member) =>
        isPublicMethod(member, source),
      );
      const methods = publicMethods.length;
      const isValueObject = node.members.some((member) =>
        isPrivateReadonlyProperty(member, source),
      );
      const isCohesiveValueObject = isCohesiveValueObjectClass(
        node,
        source,
        publicMethods,
      );
      if (isValueObject) {
        if (!isCohesiveValueObject && methods >= 8)
          add(
            'design-violation',
            'warn',
            file,
            lineOf(node),
            `value object class has ${methods} public methods`,
            { methods, symbol: node.name?.getText(source) },
          );
      } else if (methods >= 8)
        add(
          'design-violation',
          'critical',
          file,
          lineOf(node),
          `class has ${methods} public methods`,
          { methods, symbol: node.name?.getText(source) },
        );
      else if (methods >= 5)
        add(
          'design-violation',
          'high',
          file,
          lineOf(node),
          `class has ${methods} public methods`,
          { methods, symbol: node.name?.getText(source) },
        );
    }
    ts.forEachChild(node, visit);
  }
  visit(source);
}

function scanDuplicates(files) {
  const duplicateWindowLineCount = 8;
  const groups = new Map();
  for (const file of files.filter(
    (candidate) =>
      scriptExts.has(path.extname(candidate)) &&
      !isTestFile(candidate) &&
      path.basename(candidate) !== 'index.ts',
  )) {
    const text = read(file);
    if (isDuplicateScanExcludedFile(file, text)) continue;
    const lines = text
      .split('\n')
      .map((line) => line.trim())
      .filter(
        (line) =>
          line &&
          !line.startsWith('//') &&
          !line.startsWith('*') &&
          !line.startsWith('/*') &&
          !line.startsWith('*/') &&
          !line.startsWith('{/*'),
      );
    for (let i = 0; i <= lines.length - duplicateWindowLineCount; i++) {
      const window = lines.slice(i, i + duplicateWindowLineCount);
      if (isLowSignalDuplicateBlock(window)) continue;
      const block = lines
        .slice(i, i + duplicateWindowLineCount)
        .join('\n')
        .replace(/['"`][^'"`]*['"`]/g, '<str>')
        .replace(/\b\d+(\.\d+)?\b/g, '<num>');
      if (block.length < 80) continue;
      if (!groups.has(block)) groups.set(block, []);
      groups.get(block).push({ file, line: i + 1 });
    }
  }
  const candidates = [];
  for (const locations of groups.values()) {
    const uniqueFiles = [...new Set(locations.map((loc) => rel(loc.file)))];
    if (uniqueFiles.length < 4) continue;
    const first = [...locations].sort(
      (a, b) => rel(a.file).localeCompare(rel(b.file)) || a.line - b.line,
    )[0];
    candidates.push({ first, uniqueFiles: uniqueFiles.sort() });
  }

  candidates.sort(
    (a, b) =>
      b.uniqueFiles.length - a.uniqueFiles.length ||
      rel(a.first.file).localeCompare(rel(b.first.file)) ||
      a.first.line - b.first.line,
  );

  const reportedClusters = [];
  for (const candidate of candidates) {
    const candidateFiles = new Set(candidate.uniqueFiles);
    const isOverlappingCluster = reportedClusters.some((cluster) => {
      if (cluster.firstFile !== rel(candidate.first.file)) return false;
      if (Math.abs(cluster.line - candidate.first.line) > 30) return false;
      const overlap = candidate.uniqueFiles.filter((file) =>
        cluster.files.has(file),
      ).length;
      return (
        overlap / Math.min(candidate.uniqueFiles.length, cluster.files.size) >=
        0.8
      );
    });
    if (isOverlappingCluster) continue;
    reportedClusters.push({
      firstFile: rel(candidate.first.file),
      line: candidate.first.line,
      files: candidateFiles,
    });
    add(
      'near-duplicate',
      candidate.uniqueFiles.length >= 50
        ? 'high'
        : candidate.uniqueFiles.length >= 25
          ? 'warn'
          : 'info',
      candidate.first.file,
      candidate.first.line,
      `${duplicateWindowLineCount}-line block cluster appears in ${candidate.uniqueFiles.length} files`,
      {
        copies: candidate.uniqueFiles.length,
        files: candidate.uniqueFiles.slice(0, 8),
      },
    );
  }
}

