export function getContentTypeLabel(
  type: ShareableContentType | 'folder',
): string {
  switch (type) {
    case 'unit':
      return 'Unit';
    case 'pilot':
      return 'Pilot';
    case 'force':
      return 'Force';
    case 'encounter':
      return 'Encounter';
    case 'folder':
      return 'Folder';
    default:
      return 'Item';
  }
}

export function getContentTypeIcon(
  type: ShareableContentType | 'folder',
): React.ReactNode {
  switch (type) {
    case 'unit':
      return <MechIcon className="h-5 w-5" />;
    case 'pilot':
      return <PilotIcon className="h-5 w-5" />;
    case 'force':
      return <ForceIcon className="h-5 w-5" />;
    case 'encounter':
      return <EncounterIcon className="h-5 w-5" />;
    case 'folder':
      return <FolderIcon className="h-5 w-5" />;
    default:
      return <DocumentIcon className="h-5 w-5" />;
  }
}

export function formatConflictDate(isoDate: string): string {
  const date = new Date(isoDate);
  return date.toLocaleString('en-US', {
    month: 'short',
    day: 'numeric',
    year: 'numeric',
    hour: 'numeric',
    minute: '2-digit',
  });
}
