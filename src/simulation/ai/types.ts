/**
 * Simplified weapon interface for AI decision-making.
 * Contains only the fields needed to determine if a weapon can fire.
 */
export interface IWeapon {
  /** Unique weapon ID */
  readonly id: string;

  /** Weapon name */
  readonly name: string;

  /** Short range (hexes) */
  readonly shortRange: number;

  /** Medium range (hexes) */
  readonly mediumRange: number;

  /** Long range (hexes) */
  readonly longRange: number;

  /** Extreme range (hexes), when advanced range rules are enabled */
  readonly extremeRange?: number;

  /** Damage dealt on hit */
  readonly damage: number;

  /** Heat generated when fired */
  readonly heat: number;

  /** Minimum range penalty (0 = no minimum) */
  readonly minRange: number;

  /** Ammo per ton (-1 = energy weapon, no ammo needed) */
  readonly ammoPerTon: number;

  /**
   * Mounted unit location, when hydrated from catalog/unit construction data.
   * Values are source labels such as `LEFT_ARM`, `RIGHT_TORSO`, or
   * `Center Torso`; consumers normalize at the rule boundary.
   */
  readonly location?: string;

  /**
   * Optional missile fire-control flags hydrated from canonical unit critical
   * slots. When present, runner missile resolution can apply source-backed
   * Artemis cluster-table bonuses without each test hand-building helper
   * contexts.
   */
  readonly hasArtemisIV?: boolean;
  readonly hasPrototypeArtemisIV?: boolean;
  readonly hasArtemisV?: boolean;

  /** Is this weapon destroyed? */
  readonly destroyed: boolean;

  /**
   * Per `improve-bot-basic-combat-competence` task 3.1: the arc the
   * weapon is mounted in. Front is the default (and matches the
   * pre-change behavior — most 'Mech weapons are front-mounted).
   * When present, `AttackAI.selectWeapons` filters weapons by whether
   * the target hex lies in the weapon's mounted arc given the
   * attacker's facing (and optional torso twist).
   *
   * Optional so existing test fixtures and runtime wiring (which
   * build `IWeapon` without arc data) keep working — missing = Front.
   */
  readonly mountingArc?: FiringArc;

  /**
   * Represented multi-arc mount coverage. Used for vehicle sponsons,
   * unlocked turrets, and other mounts that legitimately cover more than one
   * chassis arc. Missing means legacy omnidirectional/unknown coverage.
   */
  readonly mountingArcs?: readonly FiringArc[];

  /**
   * Explicit AMS lifecycle override for represented multi-use AMS behavior.
   * Standard AMS omits this and stays unavailable after one same-phase use.
   */
  readonly amsMultiUse?: boolean;

  /** Vehicle mount location, when this weapon belongs to a vehicle. */
  readonly vehicleMountLocation?: VehicleLocation | VTOLLocation;

  /** True when the weapon is mounted in the vehicle primary turret. */
  readonly vehicleIsTurretMounted?: boolean;

  /**
   * Per `add-ai-resource-planning` (A2) design D4: optional firing-mode
   * metadata for multi-mode weapons — LB-X autocannons (cluster vs. slug),
   * Ultra / Rotary autocannons (rate of fire). When present,
   * `AIWeaponModeSelector` picks the expected-damage-maximizing mode given
   * range, the target's armor state, and the remaining heat budget.
   *
   * A weapon WITHOUT this field is single-mode — `AIWeaponModeSelector`
   * returns its default mode unchanged, so existing weapon fixtures and
   * runtime wiring need no migration (the selector is byte-identical to
   * "always default mode" for them).
   *
   * Typed as `IWeaponFiringModes` from `AIWeaponModeSelector` — declared
   * here as a structural shape to keep `types.ts` free of an import cycle
   * with the selector module.
   */
  readonly firingModes?: IWeaponFiringModes;

  /** True for represented torpedo weapons that must remain in water. */
  readonly isTorpedo?: boolean;
}

/**
 * The kind of multi-mode weapon, used by `AIWeaponModeSelector` to pick the
 * right mode-selection heuristic.
 *
 *   - `cluster-slug`: LB-X autocannon — cluster (spread / crit-seek) vs.
 *     slug (focused).
 *   - `rate-of-fire`: Ultra / Rotary autocannon — a higher rate of fire
 *     trades heat and ammo for damage.
 */
export type WeaponModeKind = 'ammo-mode' | 'cluster-slug' | 'rate-of-fire';

/**
 * One selectable firing mode of a multi-mode weapon.
 *
 * `damage`, `heat`, and `shotsPerTurn` describe the mode's per-turn cost and
 * output — they OVERRIDE the weapon's base `damage`/`heat` when the mode is
 * selected, so the combat engine resolves the chosen mode's numbers.
 */
export interface IWeaponFiringMode {
  /** Stable mode identifier, e.g. `'cluster'`, `'slug'`, `'single'`. */
  readonly id: string;
  /** Damage this mode deals per turn. */
  readonly damage: number;
  /** Heat this mode generates per turn. */
  readonly heat: number;
  /** Ammo rounds this mode consumes per turn (energy weapons: `0`). */
  readonly shotsPerTurn: number;
  /** Optional ammo-bin weapon family when mode choice changes ammo type. */
  readonly ammoWeaponType?: string;
}

/**
 * Firing-mode metadata for a multi-mode weapon — the optional `firingModes`
 * field on `IWeapon`.
 */
export interface IWeaponFiringModes {
  /** The mode-selection heuristic family this weapon belongs to. */
  readonly kind: WeaponModeKind;
  /** The id of the mode fired when mode selection is disabled. */
  readonly defaultModeId: string;
  /** The selectable modes — at least two for a genuine multi-mode weapon. */
  readonly modes: readonly IWeaponFiringMode[];
}

