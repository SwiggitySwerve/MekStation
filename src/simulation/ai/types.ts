/**
 * Retreat edge direction for bot behavior.
 */
export type RetreatEdge =
  | 'nearest'
  | 'north'
  | 'south'
  | 'east'
  | 'west'
  | 'none';

/**
 * Bot behavior settings (adapted from MekHQ's BehaviorSettings).
 * Controls when and how the bot retreats.
 */
export interface IBotBehavior {
  /**
   * Health percentage threshold to trigger retreat (0-1).
   * Default: 0.3 (retreat at 30% health)
   */
  readonly retreatThreshold: number;

  /**
   * Which map edge to retreat toward.
   * Default: 'nearest'
   */
  readonly retreatEdge: RetreatEdge;

  /**
   * Per `improve-bot-basic-combat-competence` task 1.1: heat ceiling
   * the bot tries to stay at or below when picking a fire list.
   * Above this value the bot drops the lowest damage-per-heat weapon
   * from the candidate list until projected heat fits.
   *
   * Default: 13 (corresponds to the +1 to-hit canonical heat threshold;
   * past it the bot starts taking penalties). Tuning per-difficulty
   * profiles can override this without recompiling the AI.
   */
  readonly safeHeatThreshold: number;

  /**
   * Opt-in tactical posture rule for source-backed voluntary go-prone
   * movement. When enabled, `BotPlayer` may choose a same-hex `goProne`
   * movement step instead of returning no movement when a standing unit has
   * no non-stationary legal destinations. Default/variant presets leave this
   * unset so existing AI traces remain unchanged.
   */
  readonly voluntaryGoProneWhenStationary?: boolean;

  /**
   * Per `add-ai-terrain-aware-movement` design D3: the AI difficulty tier
   * this bot plays at. The tier name keys into the AI Difficulty Tier
   * Registry (`AITierRegistry.ts`), which supplies the movement-scoring
   * parameters (pathfinder enable flag + cover / LOS-denial / terrain-cost
   * weights).
   *
   * Optional for backward compatibility: when absent, the bot resolves to
   * the `Regular` tier, which runs the legacy straight-line move scorer
   * byte-for-byte. Every existing bot, test fixture, and the swarm harness
   * keep their pre-change behavior until a caller opts into `Veteran` /
   * `Elite`. The tier is player-selectable per scenario and serializes with
   * the game-session config.
   */
  readonly tier?: AITierName;
}

/**
 * Default bot behavior settings.
 *
 * `tier: 'Regular'` is the legacy-scorer tier — `MoveAI` resolves an absent
 * `tier` to `Regular` anyway, so pinning it explicitly here is purely
 * self-documenting and keeps `DEFAULT_BEHAVIOR` aligned with the `default`
 * preset in `behaviorVariants.ts`. The bot's pre-change move scoring is
 * unchanged.
 */
export const DEFAULT_BEHAVIOR: IBotBehavior = {
  retreatThreshold: 0.3,
  retreatEdge: 'nearest',
  safeHeatThreshold: 13,
  tier: 'Regular',
};

